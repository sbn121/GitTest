
public class Ex01_Operator {
	public static void main(String[] args) {
		int x = -100;
		int result= +x;//-100
		int result2= -x;//+100
		System.out.println("(+x) result = " + result);
		System.out.println("(-x) result2 = " + result2);
		System.out.println("(-x) x*-1=" + x * -1);
		//-부호는 부호를 반전 시키는 규칙이 있음
	}

}
