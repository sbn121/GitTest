package pack02_static;

public class Ex02_Calculator {
	static int plus(int num1, int num2) {
		return num1+num2;
	}
	static int minus(int num1, int num2) {
		return num1-num2;
	}
	static int mul(int num1, int num2) {
		return num1*num2;
	}
	static int div(int num1, int num2) {
		return num1/num2;
	}
}
