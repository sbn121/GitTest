package pack06_bool;

public class Ex01_Bool {
	public static void main(String[] args) {
		//부울형(논리형) : boolean
		//true, false 만 기억 > 조건식 작성 시 사용.
		//컴퓨터는 어떠한 판단을 true와 false로만 함.
		//컴퓨터에게 어떠한 질문을 할 경우 true, false로 답이 나오는 것만 가능
		//ex) 내 이름 뭐야 > x
		boolean t = true;
		boolean f = false;
		//문자열도 변수도 표현하고 싶다
		//>
		System.out.println("변수 t에 들어있는 값 : "+ t);
		System.out.println("변수 f에 들어있는 값:" + f);
		System.out.println(t);
	}

}
